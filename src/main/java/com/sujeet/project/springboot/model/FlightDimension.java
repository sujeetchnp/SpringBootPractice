package com.sujeet.project.springboot.model;

public class FlightDimension {
    private String dFlightName;
    private Double dLength;
    private Double dWidth;
    private Double dHeight;

    public String getdFlightName() {
        return dFlightName;
    }

    public void setdFlightName(String dFlightName) {
        this.dFlightName = dFlightName;
    }

    public Double getdLength() {
        return dLength;
    }

    public void setdLength(Double dLength) {
        this.dLength = dLength;
    }

    public Double getdWidth() {
        return dWidth;
    }

    public void setdWidth(Double dWidth) {
        this.dWidth = dWidth;
    }

    public Double getdHeight() {
        return dHeight;
    }

    public void setdHeight(Double dHeight) {
        this.dHeight = dHeight;
    }
}
